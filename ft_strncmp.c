int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	count;

	count = 0;
	while (*s1 && (*s1 == *s2) && count < n - 1)
	{
		s1++;
		s2++;
		count++;
	}
	return (*s1 - *s2);
}
